    public function findMostAuthorsInCatalog()
    {
        return $this->createQueryBuilder('a')
            ->select('count(a.id) as c, a.id, a.lastName, a.firstName')
            ->innerJoin('a.isInvolvedIns', 'i')
            ->groupBy('a.id')
            ->orderBy('c', 'DESC')
            ->setMaxResults(5)
            ->getQuery()
            ->getResult()
